package replits;
/*
Given an int variable k that has already been declared, use a for loop to print a single line
 consisting of 97 asterisks. Use no variables other than k.
 */
public class replit_112 {
    public static void main(String[] args) {


        for (int k = 1; k <= 97; k++) {
            System.out.print("*");
        }


    }
}
