package replits;


import java.util.Scanner;

public class replit_loopCalculateSum {
    public static void main(String[] args) {
        Scanner scan=new Scanner(System.in);
        int num = scan.nextInt();  // 100
        int sum = 1;
        for (int i = 1; i <= num; i++) {
            sum *= i;
        }
        System.out.println(sum);











    }}
