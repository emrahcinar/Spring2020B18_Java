package officeHours.Practice_04_08_2020;

public class ForLoops_Practice {
    public static void main(String[] args) {
        /*
        for(initilization; condition;itreator )
        statements;
        execution order:
        1.initialization
        2.condition
             if true:
                 statement in the loop body
                 then iterador
                 if false:
                    stops loop
         */

        for (int i = 0; i <= 5; i += 2) {      // i++: 1,2,3,4,5
            System.out.println("Hello World " + i); // i+=2: 1,3,5
        }
        System.out.println("=========================================================================");

        for (int i = 1; i <= 5;  i ++) {
            System.out.println( i);//1 2 3 4 5

        }




























    }
}
