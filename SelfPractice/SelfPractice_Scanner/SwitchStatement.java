package SelfPractice.SelfPractice_Scanner;

public class SwitchStatement {
    public static void main(String[] args) {
        int num=8;

         switch (num){
             case 1:
                 num+=5;
                break;
                case 2:
                 num+=5;
                 break;
             case 3:
                 num+=5;
                 break;
             case 4:
                 num+=5;
                 break;
             default:
                 num+=5;
                 break;
         }

        System.out.println(num);



























    }
}
